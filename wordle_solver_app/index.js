function addMissingLetters(value) {
  const missingLetters = [];
  for (let letter of value) {
    switch (letter) {
      case "ץ":
        if (!value.includes("צ")) {
          missingLetters.push("צ");
        }
        break;
      case "צ":
        if (!value.includes("ץ")) {
          missingLetters.push("ץ");
        }
        break;
      case "ף":
        if (!value.includes("פ")) {
          missingLetters.push("פ");
        }
        break;
      case "פ":
        if (!value.includes("ף")) {
          missingLetters.push("ף");
        }
        break;
      case "ם":
        if (!value.includes("מ")) {
          missingLetters.push("מ");
        }
        break;
      case "מ":
        if (!value.includes("ם")) {
          missingLetters.push("ם");
        }
        break;
      case "ן":
        if (!value.includes("נ")) {
          missingLetters.push("נ");
        }
        break;
      case "נ":
        if (!value.includes("ן")) {
          missingLetters.push("ן");
        }
        break;
      case "ך":
        if (!value.includes("כ")) {
          missingLetters.push("כ");
        }
        break;
      case "כ":
        if (!value.includes("ך")) {
          missingLetters.push("ך");
        }
        break;
    }
  }

  value += missingLetters.join("");
  console.log(value);
  return value;
}
